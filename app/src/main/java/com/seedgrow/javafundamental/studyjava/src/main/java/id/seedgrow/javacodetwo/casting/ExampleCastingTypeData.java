package com.seedgrow.javafundamental.studyjava.src.main.java.id.seedgrow.javacodetwo.casting;

public class ExampleCastingTypeData {
    public  static void main(String[] args){

        int angka = (int) 3.14;

        System.out.println(angka);

        System.out.println();

        double mAngka = 3.14;
        //Menjadi String
        String mmAngka = String.valueOf(mAngka);
        System.out.println(mmAngka);
    }

}
