package com.seedgrow.javafundamental.studyjava.src.main.java.id.seedgrow.javacodeone.operator;

public class Relational {
    //Operator Relational "==, !=, >, <, >=, <="
    //Value TRUE or FALSE
    static int x = 10, y = 15;

    public static void main(String args[]) {

        //Operator ==
        //Relational.x = 15;
        System.out.println(x == y);

        //Operator !=
        System.out.println(x != y);

        //Operator >
        System.out.println(x > y);

        //Operator <
        System.out.println(x < y);

        //Operator >=
        //Relational.x = 15;
        System.out.println(x >= y);

        //Operator <=
        System.out.println(x <= y);
    }
}
